package Day5;

public class For_If_Else_Print_Even_Odd_Number_1_to_10 
{
	public static void main(String[] args)
	{
		for(int i =1;i<=10;i++)
		{
			if(i%2==0)
			{
				System.out.println(i +" - Even Number");
			}
			else
			{
				System.out.println(i +" - Odd Number");
			}
    	}
	}
}
